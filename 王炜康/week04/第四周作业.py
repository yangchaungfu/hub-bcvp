#待切分文本
sentence = "经常有意见分歧"

#实现全切分函数，输出根据字典能够切分出的所有的切分方式
def all_cut(sentence, Dict):
    #TODO

    # dp[i]表示第i个位置到尾部的所有切分方式
    n = len(sentence)
    dp = [[] for _ in range(n+1)]
    max_len = max(len(word) if Dict else 0 for word in Dict)#单词的最大长度
    dp[n] = [[]]
    for i in range(n-1, -1, -1):
        max_j = min(i+max_len, n)
        for j in range(i+1, max_j+1):
            if sentence[i:j] in Dict:
                for lst in dp[j]:
                    dp[i].append([sentence[i:j]] + lst)
    target = dp[0]
    return target

